/// DSMES Aceh Spacing Tokens.
/// Based on HTML design system: base=8, sm=12, md=24, lg=48, gutter=24, margin=24.
abstract final class AppSpacing {
  AppSpacing._();

  static const double xxs = 4.0;
  static const double xs = 8.0;
  static const double sm = 12.0;
  static const double md = 16.0;
  static const double lg = 24.0; // == margin == gutter == stack-md
  static const double xl = 32.0;
  static const double xxl = 48.0; // == stack-lg
  static const double page = 24.0; // horizontal page margin
}
